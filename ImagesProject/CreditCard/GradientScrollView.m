#import <QuartzCore/QuartzCore.h>

#import "GradientScrollView.h"

@implementation GradientScrollView

+ (Class)layerClass { return [CAGradientLayer class]; }

- (id)initWithCoder:(NSCoder *)aDecoder
{
    if((self = [super initWithCoder:aDecoder])) {
		CGColorRef topColor = CGColorCreateCopy([[UIColor colorWithRed:.45f green:.98f blue:.47f alpha:1] CGColor]);
		CGColorRef botColor = CGColorCreateCopy([[UIColor colorWithRed:.16f	green:.75f blue:.22f alpha:1] CGColor]);
		[(CAGradientLayer *)self.layer setColors:[NSArray arrayWithObjects:(__bridge_transfer id)topColor, (__bridge_transfer id)botColor, nil]]; // __bridge_transfer
	}
	return self;
}

@end
